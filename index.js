const firstNumber = prompt('Введіть перше число:');
const secondNumber = prompt('Введіть друге чило');

alert(`Сумма введених чисел: ${Number(firstNumber) + Number(secondNumber)}`);
alert(`Різниця введених чисел: ${firstNumber - secondNumber}`);
alert(`Множення введених чисел: ${firstNumber * secondNumber}`);
alert(`Ділення введених чисел: ${firstNumber / secondNumber}`);
alert(`Остача від ділення введених чисел: ${firstNumber % secondNumber}`);